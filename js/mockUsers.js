// 랜덤 사용자 선택 함수
function getRandomUsers(count) {
    const shuffled = [...mockUsers].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

// ID로 사용자 찾기
function getUserById(id) {
    return mockUsers.find((user) => user.id === id);
}

// 레벨별 사용자 필터링
function getUsersByLevel(level) {
    return mockUsers.filter((user) => user.level === level);
}

// 활동 점수 기준 상위 사용자
function getTopUsers(count = 10) {
    return [...mockUsers].sort((a, b) => b.points - a.points).slice(0, count);
}

// 최근 활동 사용자
function getRecentlyActiveUsers(count = 10) {
    return [...mockUsers]
        .sort((a, b) => new Date(b.lastActive) - new Date(a.lastActive))
        .slice(0, count);
}